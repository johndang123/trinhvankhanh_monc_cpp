#include <iostream>

int main() {
    // Khai báo và khởi tạo hai số nguyên
    int num1 = 50;
    int num2 = 100;
    
    // Tính tổng
    int sum = num1 + num2;
    
    // In tổng ra màn hình
    std::cout << "Tổng của " << num1 << " và " << num2 << " là: " << sum << std::endl;
    
    return 0;
}
