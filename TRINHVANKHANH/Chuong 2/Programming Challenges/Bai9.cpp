#include <iostream>

int main() {
    std::cout << "Kích thước của kiểu char là " << sizeof(char) << " byte." << std::endl;
    std::cout << "Kích thước của kiểu int là " << sizeof(int) << " byte." << std::endl;
    std::cout << "Kích thước của kiểu float là " << sizeof(float) << " byte." << std::endl;
    std::cout << "Kích thước của kiểu double là " << sizeof(double) << " byte." << std::endl;

    return 0;
}
